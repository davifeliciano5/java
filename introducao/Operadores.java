package estudos.java.introducao;

public class Operadores {
    public static void main(String[] args) {
        System.out.println(10 + 10);
        System.out.println(10 - 10);
        System.out.println(10 / 10);
        System.out.println(10 == 10);
        System.out.println(10 > 10);
        System.out.println(10 < 10);
        System.out.println(10 != 10);
        System.out.println(10 % 10);

        System.out.println(10 > 11 && 11>10);
        System.out.println(10 > 11 || 11>10);
    }
}
